#import "UIColor+SelfDefinedColors.h"

@implementation UIColor (SelfDefinedColors)

+ (UIColor *)goldColor {
    return [UIColor colorWithRed:199.0/255.0 green:160.0/255.0 blue:74.0/255.0 alpha:1.0];
}

+ (UIColor *)darkColor {
    return [UIColor colorWithRed:23/255.0 green:23/255.0 blue:23/255.0 alpha:1.0];
}


@end
